
// Arithmetic Operators
let a = 10;
let b = 5;

// Addition
let add = a + b; 
console.log("add:", add);

// Subtraction
let subtract = a - b; 
console.log("subtract:", subtract);

// Multiplication
let Product = a * b; 
console.log("Product:", Product);

// Division
let divide = a / b; 
console.log("divide:", divide);

// Modulus
let Modulus = a % b; 
console.log("Modulus:", Modulus);

// Exponentiation
let Power = a ** b; 
console.log("Power:", Power);

//increment
var s = 5;

// Pre increment
var preincrement = ++s; 
console.log("value of preincrement:", preincrement);

// Post increment
var l = 8;
var postincrement = l++; 
console.log("value of Postincrement:", postincrement); 
console.log("New value of l:", l);


//decrement
var s = 10;

// Pre decrement
var predecrement = --s; 
console.log("value of Predecrement:", predecrement);

// Post decrement
var l = 6;
var postdecrement = l--; 
console.log("value of Postdecrement:", postdecrement); 
console.log("New value of l:", l);


//if statement
var age = 18;

